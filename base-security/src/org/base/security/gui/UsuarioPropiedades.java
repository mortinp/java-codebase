    private void btnAgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAgregarMouseClicked
        for(Object o:lstNoMiembros.getSelectedValues())
        {
            DefaultListModel model = (DefaultListModel)lstMiembros.getModel();
            model.addElement(o);
            model = (DefaultListModel)lstNoMiembros.getModel();
            model.removeElement(o);
        }
    }//GEN-LAST:event_btnAgregarMouseClicked

    private void btnEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarMouseClicked
        for(Object o:lstMiembros.getSelectedValues())
        {
            DefaultListModel model = (DefaultListModel)lstNoMiembros.getModel();
            model.addElement(o);
            model = (DefaultListModel)lstMiembros.getModel();
            model.removeElement(o);
        }
    }//GEN-LAST:event_btnEliminarMouseClicked

