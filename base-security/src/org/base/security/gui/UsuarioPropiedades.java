    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        List<String> list = new ArrayList<String>(lstMiembros.getModel().getSize());
        for(int i = 0;i < lstMiembros.getModel().getSize();i++)
        {
            list.add((String)lstMiembros.getModel().getElementAt(i));
        }
        new DbServise().AsignarRoles(list, usuario);
        //JOptionPane.showMessageDialog(null, "Producto actualizado satisfactoriamente", "!Informaci√≥n!", JOptionPane.INFORMATION_MESSAGE);
        this.dialogo.dispose();
    }//GEN-LAST:event_btnAceptarActionPerformed

