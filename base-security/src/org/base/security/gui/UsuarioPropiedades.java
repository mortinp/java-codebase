    public void llenar()
    {
        DefaultListModel model = new DefaultListModel();
        DbServise db = new DbServise();
        List<String> usuarios = db.rolesUsuario(usuario);
        for(String usuario:usuarios)
        {
            model.addElement(usuario);
        }
        lstMiembros.setModel(model);
        
        DefaultListModel model1 = new DefaultListModel();
        usuarios = db.rolesNoUsuario(usuario);
        for(String usuario:usuarios)
        {
            model1.addElement(usuario);
        }
        lstNoMiembros.setModel(model1);
    }
    
